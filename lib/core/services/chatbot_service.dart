      return _getRandomResponse(_supportiveResponses);
    }

    // –ü–æ–∑–∏—Ç–∏–≤–Ω—ã–µ —ç–º–æ—Ü–∏–∏
    if (_containsAny(message, ['—Ö–æ—Ä–æ—à–æ', '—Ä–∞–¥–æ—Å—Ç—å', '—Å—á–∞—Å—Ç–ª–∏–≤', '–æ—Ç–ª–∏—á–Ω–æ', '–≤–µ—Å–µ–ª–æ', '–∫—Ä—É—Ç–æ'])) {
      return _getRandomResponse(_positiveResponses);
    }

    // –í–æ–ø—Ä–æ—Å—ã –æ –ø–æ–º–æ—â–∏
    if (_containsAny(message, ['–ø–æ–º–æ–≥–∏', '–ø–æ–º–æ—â—å', '—á—Ç–æ –¥–µ–ª–∞—Ç—å', '–∫–∞–∫ –±—ã—Ç—å', '—Å–æ–≤–µ—Ç'])) {
      return _getRandomResponse(_helpResponses);
    }

    // –°—Ç—Ä–µ—Å—Å –∏ —Ä–∞–±–æ—Ç–∞
    if (_containsAny(message, ['—Å—Ç—Ä–µ—Å—Å', '—Ä–∞–±–æ—Ç–∞', '—É—á–µ–±–∞', '—ç–∫–∑–∞–º–µ–Ω', '–¥–µ–¥–ª–∞–π–Ω'])) {
      return _getRandomResponse(_stressResponses);
    }

    // –ü—Ä–æ–±–ª–µ–º—ã —Å–æ —Å–Ω–æ–º
    if (_containsAny(message, ['—Å–æ–Ω', '–±–µ—Å—Å–æ–Ω–Ω–∏—Ü–∞', '–Ω–µ —Å–ø–ª—é', '—É—Å—Ç–∞–ª'])) {
      return _getRandomResponse(_sleepResponses);
    }

    // –û—Ç–Ω–æ—à–µ–Ω–∏—è
    if (_containsAny(message, ['–æ—Ç–Ω–æ—à–µ–Ω–∏—è', '–¥—Ä—É–∑—å—è', '—Å–µ–º—å—è', '–∫–æ–Ω—Ñ–ª–∏–∫—Ç', '—Å—Å–æ—Ä–∞'])) {
      return _getRandomResponse(_relationshipResponses);
    }

    // –î–µ—Ñ–æ–ª—Ç–Ω—ã–π –æ—Ç–≤–µ—Ç, –µ—Å–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤
    return _getRandomResponse(_defaultResponses);
  }

